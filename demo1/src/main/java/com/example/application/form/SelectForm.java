package com.example.application.form;

public class SelectForm {
	private int empid;
	private String a_name;
	private String a_branch;
	private int a_age;
	private String hobname;
	
	public int getEmpid() {
		return empid;
	}
	public void setEmpid(int empid) {
		this.empid = empid;
	}
	public String getA_name() {
		return a_name;
	}
	public void setA_name(String a_name) {
		this.a_name = a_name;
	}
	public String getA_branch() {
		return a_branch;
	}
	public void setA_branch(String a_branch) {
		this.a_branch = a_branch;
	}
	public int getA_age() {
		return a_age;
	}
	public void setA_age(int a_age) {
		this.a_age = a_age;
	}
	public String getHobname() {
		return hobname;
	}
	public void setHobname(String hobname) {
		this.hobname = hobname;
	}
}
